let x 
let y
x = Math.floor(Math.random() * 100-1)
y = Math.floor(Math.random() * 50-1)

// Get The Sum
console.log(`${x} + ${y} = ${x+y}`)

// Get The Difference
console.log(`${x} - ${y} = ${x-y}`)

// Get The Product
console.log(`${x} * ${y} = ${x*y}`)

//  Get The Quotient
console.log(`${x} / ${y} = ${x/y}`)

// Get The Reminder
console.log(`${x} % ${y} = ${x%y}`)